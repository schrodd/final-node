import mongoose from "mongoose";

const userCollection = "users";
const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  }
});

const UserModel = mongoose.model(userCollection, userSchema);

export default UserModel;
